require 'spec_helper'

describe Cell do 
    describe 'cell states' do 
        context 'when a cell has < 2 live neighbors' do 
            xit 'is dead' 
        end

        context 'when a cell has 2 or 3 live neighbors' do 
            xit 'is alive'
        end

        context 'when a cell has exactly 3 live neighbors' do 
            xit 'is alive'
        end

        context 'when a cell has > 3 live neighbors' do 
            xit 'is dead'
        end
    end
end